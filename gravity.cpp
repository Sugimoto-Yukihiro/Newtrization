#include "main.h"
#include "gravity.h"

//*****************************************************************************
// マクロ定義
//*****************************************************************************
#define GRAVITY			(-9.8f)		

//=============================================================================
// 初期化処理
//=============================================================================
void CGravity::Init()
{
	m_Position = ZERO_VECTOR2;
	m_GravityWait = GRAVITY;
	m_nFlameCount = 0;
}
//=============================================================================
// 終了処理
//=============================================================================


//=============================================================================
// 更新処理
//=============================================================================

void CGravity::Update(DIRECTION Direction)
{
	if (!m_bFall) return;

	m_nFlameCount++;

	if (Direction == GRAVITY_DEFAULT)
	{
		// 重力でプレイヤーを下に動かす処理
		m_Position.y += m_GravityWait * m_nFlameCount;
	}
	else if (Direction == GRAVITY_LEFT)
	{
		// 重力でプレイヤーを左に動かす処理
		m_Position.x -= m_GravityWait * m_nFlameCount;
	}
}